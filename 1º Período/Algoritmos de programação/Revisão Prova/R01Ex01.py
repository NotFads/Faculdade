A = int(input('Entre com o valor A: '))
B = int(input('Entre com o valor B: '))
C = int(input('Entre com o valor C: '))
R = A + B
print(f'A soma de A e B é: {R}')
if R > C:
    print(f'A soma dos valores A e B é maior do que o valor C.')
else:
    print(f'A soma dos valores A e B é menor do que o valor C.')